#definição
saldo = 500
print(saldo)

#substituição de valor
saldo = 200
print(saldo)

#adição
saldo += 10
print(saldo)

#subtração
saldo -= 5
print(saldo)

#multiplicação
saldo *= 10
print(saldo)

#divisão inteira
#IMPORTANTE - a divisão inteira só funciona quando o numero dividido é inteiro por essa
#questão a divisão inteira está primeiro no código, após a divisão com ponto flutuante
#todas as operações posteriores sairão com ponto flutuante
saldo //= 2
print(saldo)

#divisão com ponto flutuante
saldo /= 2
print(saldo)

#modulo
saldo %= 10
print(saldo)

#exponenciação
saldo **= 2
print(saldo)